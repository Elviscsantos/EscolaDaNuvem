# Calculadora de Volume de uma caixa retangular
# Comprimento: 12 cm, Largura: 14 cm, Altura: 20 cm
# Exibe o volume em cm³

def main():
    comprimento = 12  # cm
    largura = 14      # cm
    altura = 20       # cm
    volume = comprimento * largura * altura
    print(f"Comprimento: {comprimento} cm")
    print(f"Largura: {largura} cm")
    print(f"Altura: {altura} cm")
    print(f"Volume: {volume} cm³")

if __name__ == "__main__":
    main()
